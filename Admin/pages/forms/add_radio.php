<?php
// Include the database configuration file
include 'dbconfig.php';

// Initialize the result message variable
$resultMessage = '';

// Process form submission
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Get radio model from the form
    $radio_model = mysqli_real_escape_string($connection, $_POST['radio_model']);

    // Check if the entry already exists
    $check_query = "SELECT * FROM radio WHERE radio_model = ?";
    $check_stmt = mysqli_prepare($connection, $check_query);

    if ($check_stmt) {
        mysqli_stmt_bind_param($check_stmt, "s", $radio_model);
        mysqli_stmt_execute($check_stmt);
        mysqli_stmt_store_result($check_stmt);

        if (mysqli_stmt_num_rows($check_stmt) > 0) {
            $resultMessage = "Error: Radio Model $radio_model already exists!";
        } else {
            // Prepare and execute the SQL statement with a prepared statement
            $insert_query = "INSERT INTO radio (radio_model) VALUES (?)";
            $insert_stmt = mysqli_prepare($connection, $insert_query);

            if ($insert_stmt) {
                mysqli_stmt_bind_param($insert_stmt, "s", $radio_model);
                $result = mysqli_stmt_execute($insert_stmt);

                $resultMessage = $result ? "Radio Model $radio_model added successfully!" : "Error: " . mysqli_error($connection);

                mysqli_stmt_close($insert_stmt);
            } else {
                $resultMessage = "Error: " . mysqli_error($connection);
            }
        }

        mysqli_stmt_close($check_stmt);
    } else {
        $resultMessage = "Error: " . mysqli_error($connection);
    }
}
