    $('.edit-user').click(function() {
        var userId = $(this).data('id');
        $('#userId').val(userId); // Set the user ID in the hidden input field

        // Fetch user details via AJAX
        $.ajax({
            url: 'fetch_user.php', // Path to PHP script that fetches user details
            type: 'GET',
            data: { id: userId },
            success: function(response) {
                var userData = JSON.parse(response);
                $('#editName').val(userData.Name);
                $('#editEmail').val(userData.email);
                $('#editRole').val(userData.role);
            },
            error: function(xhr, status, error) {
                console.error("Error fetching user details:", error);
            }
        });
    });

    // Save changes button click event
    $('#saveChangesBtn').click(function() {
        // Gather form data
        var formData = $('#editUserForm').serialize();
        
        // Submit the form via AJAX
        $.ajax({
            url: 'update_user.php', // Path to PHP script that updates user details
            type: 'POST',
            data: formData,
            success: function(response) {
                console.log("User details updated successfully.");
                // Reload the page after updating user details
                window.location.reload();
            },
            error: function(xhr, status, error) {
                console.error("Error updating user details:", error);
            }
        });

        // Close the modal
        $('#editUserModal').modal('hide');
    });
});
